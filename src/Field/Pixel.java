package Field;

public class Pixel {
    private double x;
    private double y;

    public Pixel(double setX, double setY) {
        x = setX;
        y = setY;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public void setX(double setX) {
        x = setX;
    }

    public void setY(double setY) {
        y = setY;
    }
}
